#!/usr/bin/env node
/**
 * vendor-wasm-cc1.mjs — copy the wasm-retro-cc "Show Assembly" bundle into
 * this repo's src/web/public/wasm-cc1/ so the playground can fetch the
 * compiler as static assets.
 *
 * Source: a sibling wasm-retro-cc checkout's dist/show-asm/ tree, produced
 * by `node scripts/build-show-asm-bundle.mjs` in that repo.
 *
 *   ../wasm-retro-cc/dist/show-asm/cc1.mjs            (~147 KB)
 *   ../wasm-retro-cc/dist/show-asm/cc1.wasm           (~12 MB raw / 3.4 MB br)
 *   ../wasm-retro-cc/dist/show-asm/sysroot.bin        (~1.1 MB raw / 185 KB br)
 *   ../wasm-retro-cc/dist/show-asm/sysroot.index.json (~8 KB)
 *
 * Destination: src/web/public/wasm-cc1/ — committed to git the same way
 * src/web/public/wasm-rez/* is, so CI doesn't need an emsdk runtime.
 *
 * Why vendor rather than fetch at build time:
 *   - cv-mac CI is a pure static site build; pulling cross-repo artefacts
 *     adds a moving part and a network dependency the page doesn't have.
 *   - The bundle is large but rarely changes; binary diffs in git are fine
 *     given we already vendor the wasm-rez blob (~317 KB) and the
 *     system755-vibe disk chunks (~32 MB).
 *
 * Usage:
 *   node scripts/vendor-wasm-cc1.mjs
 *     # default: ../wasm-retro-cc/dist/show-asm/
 *   node scripts/vendor-wasm-cc1.mjs /path/to/wasm-retro-cc/dist/show-asm/
 */
import {
  copyFileSync,
  mkdirSync,
  readFileSync,
  rmSync,
  statSync,
  writeFileSync,
} from "node:fs";
import { createHash } from "node:crypto";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO = resolve(__dirname, "..");

const FILES = ["cc1.mjs", "cc1.wasm", "sysroot.bin", "sysroot.index.json"];

const srcArg = process.argv[2];
const SRC = resolve(
  srcArg ?? resolve(REPO, "../wasm-retro-cc/dist/show-asm"),
);
const DEST = resolve(REPO, "src/web/public/wasm-cc1");

for (const f of FILES) {
  const p = resolve(SRC, f);
  if (!statSync(p, { throwIfNoEntry: false })) {
    console.error(
      `error: missing ${p}\n` +
        `  run \`node scripts/build-show-asm-bundle.mjs\` in the ` +
        `wasm-retro-cc repo first.`,
    );
    process.exit(2);
  }
}

rmSync(DEST, { recursive: true, force: true });
mkdirSync(DEST, { recursive: true });

const manifest = {
  generated: new Date().toISOString(),
  source: SRC,
  files: {},
};

let total = 0;
for (const f of FILES) {
  const srcPath = resolve(SRC, f);
  const destPath = resolve(DEST, f);
  copyFileSync(srcPath, destPath);
  const bytes = readFileSync(destPath);
  const sha = createHash("sha256").update(bytes).digest("hex");
  manifest.files[f] = { size: bytes.length, sha256: sha };
  total += bytes.length;
  console.log(`[vendor] ${f.padEnd(20)} ${bytes.length} B  sha256=${sha.slice(0, 16)}…`);
}

writeFileSync(
  resolve(DEST, "VENDORED.md"),
  `# vendored wasm-cc1 bundle

Generated by \`scripts/vendor-wasm-cc1.mjs\` on ${manifest.generated}.
Source: \`${manifest.source}\`.

| File | Size (B) | SHA-256 |
| --- | --- | --- |
${FILES.map((f) => {
  const m = manifest.files[f];
  return `| \`${f}\` | ${m.size} | \`${m.sha256}\` |`;
}).join("\n")}

To refresh: in the wasm-retro-cc repo, run
\`node scripts/build-show-asm-bundle.mjs\`, then back here run
\`node scripts/vendor-wasm-cc1.mjs\` and commit the result.

See cv-mac issue #64 and wasm-retro-cc issue #17 for the cross-repo
contract and the Show Assembly feature spec.
`,
);

console.log(`[vendor] total: ${total} B`);
console.log(`[vendor] wrote ${DEST}`);
